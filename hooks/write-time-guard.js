#!/usr/bin/env node
'use strict';
/**
 * PostToolUse hook (Edit|Write|MultiEdit) — write-time quality digest.
 *
 * Problem: review-skill knowledge (security, perf, architecture, tests) only
 * enters context when a review skill is invoked — AFTER the code is written.
 * This hook moves the essence to the moment of editing: when a file of a known
 * AREA (backend / data-query / frontend-ui / test / schema) is touched, it injects
 * that area's digest as additionalContext.
 *
 * Portability: the digest is a MERGE of two layers —
 *   1. base    = <plugin>/rules/<area>.md          (stack-agnostic frame, shipped)
 *   2. project = <cwd>/.guardrails/<area>.md        (project-aware, generated by init-guards)
 * The project layer is what carries real signal (canonical helper names, real lint
 * rules). Without it, only the base frame injects — still useful, weaker.
 *
 * Anti-noise: each area injects AT MOST once per session (state in a temp file keyed
 * by session_id). Robust by design: never throws, always exits 0, never blocks the edit.
 */

const fs = require('node:fs');
const os = require('node:os');
const path = require('node:path');

const PLUGIN_ROOT = path.resolve(__dirname, '..');

// Server-side language extensions — the set eligible for PATH-based backend/data
// classification (an Edit often ships only the changed snippet, so the path is a
// more reliable signal than sniffing the fragment for a keyword).
const SERVER_LANG_EXT = /\.(ts|js|mjs|cjs|py|go|rs|rb|java|kt|php|ex|exs|cs)$/;

/** Map an edited file to a rule area (or null). Path first, then content sniff. */
function areaFor(filePath, content) {
  const p = String(filePath || '').replace(/\\/g, '/');
  if (!p) return null;

  // Vendored / generated — never nag.
  if (/(^|\/)(node_modules|dist|build|\.next|vendor|__generated__)\//.test(p)) return null;

  // Tests (JS/TS/Python/Go).
  if (/\.(test|spec)\.[jt]sx?$/.test(p) || /(^|\/)test_[^/]+\.py$/.test(p) || /_test\.py$/.test(p) ||
      /_test\.go$/.test(p)) {
    return 'tests';
  }

  // Schema / migrations.
  if (/schema\.prisma$/.test(p) || /(^|\/)migrations?\//.test(p)) return 'schema';
  if (/\.sql$/.test(p)) return 'schema';

  // Frontend UI: components + stylesheets (skip shadcn/ui primitives).
  if (/\.(tsx|jsx|vue|svelte)$/.test(p) && !/(^|\/)components\/ui\//.test(p)) return 'frontend-ui';
  if (/\.(css|scss|sass|less)$/.test(p)) return 'frontend-ui';

  // PATH-based backend/data classification (before content sniff): a server-lang file
  // living in a data/api directory classifies even when the edited snippet has no keyword.
  if (SERVER_LANG_EXT.test(p)) {
    if (/(^|\/)(models?|repositor(y|ies)|queries|dao|db)(\/|$)/.test(p)) return 'data-query';
    if (/(^|\/)(server|api|routes?|handlers?|controllers?|services?|middleware)(\/|$)/.test(p)) {
      return 'backend-api';
    }
  }

  // Content sniff — fallback when the path doesn't decide.
  if (SERVER_LANG_EXT.test(p)) {
    const c = String(content || '');
    // SQL / ORM data access → data-query. Includes Rust (sqlx/diesel/sea_orm) and Go (gorm/sqlx).
    if (/\b(SELECT|INSERT|UPDATE|DELETE)\b/i.test(c) ||
        /(prisma\.|\.findMany\(|\.query\(|session\.query|db\.|repository\.|\.aggregate\()/.test(c) ||
        /(sqlx::|diesel::|sea_orm|database\/sql|sql\.DB|gorm\.|sqlx\.)/.test(c)) {
      return 'data-query';
    }
    // HTTP handler / route → backend-api. Includes Rust (axum/actix) and Go (net/http/gin/echo/fiber/chi).
    if (/(router|procedure|@app\.(route|get|post)|app\.(get|post|put|delete)\(|fastapi|express|http\.HandlerFunc|def handler|route\.(get|post))/i.test(c) ||
        /(axum::|actix_web|Router::new|#\[(get|post|put|delete)|async fn .*(Request|Response))/.test(c) ||
        /(http\.ResponseWriter|gin\.Context|echo\.Context|fiber\.Ctx|mux\.|chi\.)/.test(c)) {
      return 'backend-api';
    }
  }
  return null;
}

// Cross-cutting digests without a natural "file area" — triggered by CONTENT, not path.
// error-handling and performance apply to any code file; they inject in ADDITION to the
// primary area (each still deduped to 1x/session in main).
const CODE_EXT = /\.(ts|tsx|js|jsx|mjs|cjs|py|go|rs|rb|java|kt|php|ex|exs|cs|vue|svelte)$/;

/** Content-triggered extra areas (error-handling / performance). Returns [] or a subset. */
function contentAreas(filePath, content) {
  const p = String(filePath || '').replace(/\\/g, '/');
  if (!p || !CODE_EXT.test(p)) return [];
  if (/(^|\/)(node_modules|dist|build|\.next|vendor|__generated__)\//.test(p)) return [];
  const c = String(content || '');
  const out = [];
  if (/\b(catch|except|rescue)\b|Result<|if err != nil/.test(c)) out.push('error-handling');
  // Loop body doing I/O (await/fetch/query per iteration) or a spin loop.
  if (/(for\b|\.forEach\(|\.map\()[\s\S]{0,120}?\b(await|fetch|query)\b/.test(c) ||
      /while\s*\(\s*true\s*\)/.test(c)) {
    out.push('performance');
  }
  return out;
}

/** All areas to inject for an edit: the primary area plus any content-triggered extras. */
function allAreasFor(filePath, content) {
  const primary = areaFor(filePath, content);
  const areas = primary ? [primary] : [];
  for (const a of contentAreas(filePath, content)) {
    if (!areas.includes(a)) areas.push(a);
  }
  return areas;
}

/**
 * Best-effort read of the just-written content for the sniff.
 * MultiEdit ships `edits[]` (each with new_string) instead of a single `new_string`.
 * The 20KB cap is a safety bound only — with PATH-based classification in front, a
 * keyword past 20KB no longer decides the common case.
 */
function readContent(payload, filePath) {
  const ti = payload && payload.tool_input;
  if (ti && typeof ti.content === 'string') return ti.content;
  if (ti && typeof ti.new_string === 'string') return ti.new_string;
  if (ti && Array.isArray(ti.edits)) {
    return ti.edits.map((e) => (e && typeof e.new_string === 'string' ? e.new_string : '')).join('\n');
  }
  try {
    return fs.readFileSync(filePath, 'utf8').slice(0, 20000);
  } catch {
    return '';
  }
}

/** Read base rule (plugin) + project overlay, merge. Returns '' if neither exists. */
function digestFor(area, projectDir) {
  const parts = [];
  try {
    parts.push(fs.readFileSync(path.join(PLUGIN_ROOT, 'rules', `${area}.md`), 'utf8').trim());
  } catch {
    // base rule not written yet — fine.
  }
  try {
    const overlay = fs.readFileSync(path.join(projectDir, '.guardrails', `${area}.md`), 'utf8').trim();
    if (overlay) parts.push(`[project overlay · .guardrails/${area}.md]\n${overlay}`);
  } catch {
    // no project overlay — base frame only.
  }
  return parts.filter(Boolean).join('\n\n');
}

function readStdin() {
  try {
    return fs.readFileSync(0, 'utf8');
  } catch {
    return '';
  }
}

function main() {
  try {
    const payload = JSON.parse(readStdin() || '{}');
    const filePath = payload && payload.tool_input && payload.tool_input.file_path;
    const projectDir = String((payload && payload.cwd) || process.cwd());
    const areas = allAreasFor(filePath, readContent(payload, filePath));

    if (areas.length) {
      // Dedupe per session: each area injects at most once. State in temp — I/O failure
      // here degrades to "inject again", never to "break the hook".
      const sessionId = String((payload && payload.session_id) || 'nosession').replace(/[^\w-]/g, '');
      const stateFile = path.join(os.tmpdir(), `guardrails-write-time-${sessionId}.json`);
      let seen = [];
      try {
        seen = JSON.parse(fs.readFileSync(stateFile, 'utf8'));
        if (!Array.isArray(seen)) seen = [];
      } catch {
        seen = [];
      }

      const digests = [];
      const freshlySeen = [];
      for (const area of areas) {
        if (seen.includes(area) || freshlySeen.includes(area)) continue;
        const digest = digestFor(area, projectDir);
        if (digest) {
          digests.push(digest);
          freshlySeen.push(area);
        }
      }

      if (digests.length) {
        try {
          fs.writeFileSync(stateFile, JSON.stringify([...seen, ...freshlySeen]));
        } catch {
          // best-effort
        }
        process.stdout.write(
          JSON.stringify({
            hookSpecificOutput: {
              hookEventName: 'PostToolUse',
              additionalContext: digests.join('\n\n---\n\n'),
            },
          }),
        );
      }
    }
  } catch {
    // best-effort: never disturbs the edit.
  }
  process.exit(0);
}

// Exported for tests; auto-run only when invoked directly as the hook.
module.exports = { areaFor, contentAreas, allAreasFor, readContent, digestFor };

if (require.main === module) {
  main();
}
